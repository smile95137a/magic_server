package com.qiyuan.web.entity;

public class Poem {
    private String sequence;

    private String title;

    private String zodiac;

    private String fortune;

    private String poem;

    private String meaning;

    private String dongponote;

    private String bixiannote;

    private String explanation;

    private Byte sort;

    public String getSequence() {
        return sequence;
    }

    public void setSequence(String sequence) {
        this.sequence = sequence;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getZodiac() {
        return zodiac;
    }

    public void setZodiac(String zodiac) {
        this.zodiac = zodiac;
    }

    public String getFortune() {
        return fortune;
    }

    public void setFortune(String fortune) {
        this.fortune = fortune;
    }

    public String getPoem() {
        return poem;
    }

    public void setPoem(String poem) {
        this.poem = poem;
    }

    public String getMeaning() {
        return meaning;
    }

    public void setMeaning(String meaning) {
        this.meaning = meaning;
    }

    public String getDongponote() {
        return dongponote;
    }

    public void setDongponote(String dongponote) {
        this.dongponote = dongponote;
    }

    public String getBixiannote() {
        return bixiannote;
    }

    public void setBixiannote(String bixiannote) {
        this.bixiannote = bixiannote;
    }

    public String getExplanation() {
        return explanation;
    }

    public void setExplanation(String explanation) {
        this.explanation = explanation;
    }

    public Byte getSort() {
        return sort;
    }

    public void setSort(Byte sort) {
        this.sort = sort;
    }
}